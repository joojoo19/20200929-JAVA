package chap02.type;

public class ShortType {
	public static void main(String[] args) {
		// short : 2byte (계산기에서 word)
		  
		short shortValue;
		shortValue1 = 32767; // 최대값
		System.out.println(shortValue1);
		
		// shortValue1 = 32768; 최대값 초과
		
		shortValue1 = -32768; // 최소값
		System.out.println(shortValue1);
	}
}